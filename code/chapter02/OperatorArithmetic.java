/**
 * 算术运算符
 */
public class OperatorArithmetic {
    public static void main(String[] args) {
        int a = 3;
        System.out.println(+a); // 3
        System.out.println(-a); // -3
        a++;
        System.out.println(a);//4
        ++a;
        System.out.println(a);//5
        a--;
        System.out.println(a);//4
        --a;
        System.out.println(a);//3
    }
}

