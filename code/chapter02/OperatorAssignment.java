/**
 * 赋值运算符
 */
public class OperatorAssignment {
    public static void main(String[] args) {
        int a = 0;
        a = 3;
        System.out.println(a); // 3
        a += 3;
        System.out.println(a); // 6
        a -= 3;
        System.out.println(a);// 3
        a *= 3;
        System.out.println(a);// 9
        a /= 3;
        System.out.println(a);// 3
        a %= 2;
        System.out.println(a);// 1
    }
}

